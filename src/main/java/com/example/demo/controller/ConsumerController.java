@RequestMapping("/consumer")
@RestController
public class ConsumerController {
    @Autowired
    private RestTemplate restTemplate;

    @RequestMapping("/get_user")
    public String index(){
        return restTemplate.getForObject("http://provider/api/producer/get_user", String.class);
    }
}
